package com.NovBatch.JavaDemo;

public class SwitchCondtionalStatement {

	public static void main(String[] args) {
		
		 int number = 10;  
		    //Switch expression  
		 
		    switch(number){  
		    
		    //Case statements  
		    case 10: System.out.println("10");  
		    break;  
		    case 20: System.out.println("20");  
		    break;  
		    case 30: System.out.println("30");  
		    break;  
		      
		    default:System.out.println("Number Not in 10, 20 or 30");  
		    } 
		
		char ch='d';    
	    
		switch(ch)  
	    {  
	        case 'a':   
	            System.out.println("vowel");  
	            break;  
	        case 'e':   
	            System.out.println("vowel");  
	            break;  
	        case 'i':   
	            System.out.println("vowel");  
	            break;  
	        case 'o':   
	            System.out.println("vowel");  
	            break;  
	        case 'u':   
	            System.out.println("vowel");  
	            break;  
	        case 'A':   
	            System.out.println("Vowel");  
	            break;  
	        case 'E':   
	            System.out.println("Vowel");  
	            break;  
	        case 'I':   
	            System.out.println("Vowel");  
	            break;  
	        case 'O':   
	            System.out.println("Vowel");  
	            break;  
	        case 'U':   
	            System.out.println("Vowel");  
	            break;  
	        default:   
	            System.out.println("Consonant");  
	    }  
	}

}
